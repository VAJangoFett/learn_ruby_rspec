def add (num1,num2)
	num1 + num2
end

def subtract (num1,num2)
	num1 - num2
end

def sum (number)
	count = 0
		number.each do |num|
			count = count + num
		end
	return count.to_i
end

def multiply (num1,num2)
	num1 * num2
end

def power (num1,num2)
	num1 ** num2
end

